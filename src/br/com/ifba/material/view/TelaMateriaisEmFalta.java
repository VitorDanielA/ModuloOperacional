    private void atualizarMaterial(List<Material> listaMaterial){
        this.listaTabela =  new DefaultTableModel(null, new String [] {"materiais", "Aviso"});
        
        for(Material al: listaMaterial){
           if(Integer.parseInt(al.getEstoqueAtual()) <= Integer.parseInt(al.getEstoqueMinimo())){
            listaTabela.addRow(new Object[]{al.getNome(), "material com quantidade minima ou inferior!"});
            }
        }
        
        this.tblTabela.setModel(this.listaTabela);
    }
    

